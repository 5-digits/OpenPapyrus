			//rcode = auth_answr.Rcode;
			//DRVS.GetErrText(rcode.ToLong(), buf);
			DRVS.GetErrText(result, buf);
			msg.CatChar(':').Space().Cat("Error Code ").Cat(/*auth_answr.Rcode*/result).CatChar(':').Cat(buf);

int PPDrvSberTrmnl::Init(const char * pLibPath)
{
	int ok = 1;

	if(P_Lib) {
		CardAuth = 0;
		CloseDay = 0;
		TestPinpad = 0;
		//GlobalFree = 0;
		ZDELETE(P_Lib);
	}
	THROWERR(pLibPath, SBRBNK_ERR_NODLLPATH);
	P_Lib = new SDynLibrary(pLibPath);
	THROWERR(P_Lib && P_Lib->IsValid(), SBRBNK_ERR_DLLFILENOTFOUND);
	CardAuth = (CardAuthProc)P_Lib->GetProcAddr("_card_authorize");
	CloseDay = (CloseDayProc)P_Lib->GetProcAddr("_close_day");
	TestPinpad = (TestPinpadProc)P_Lib->GetProcAddr("_TestPinpad");
	//GlobalFree = (GlobalFreeProc)P_Lib->GetProcAddr("GlobalFree");
	THROWERR(P_Lib && CardAuth && CloseDay && TestPinpad /*&& GlobalFree*/, SBRBNK_ERR_DLLFILENOTFOUND);

	CATCH
		ok = 0;
		CardAuth = 0;
		CloseDay = 0;
		TestPinpad = 0;
		//GlobalFree = 0;
		if(P_Lib)
			ZDELETE(P_Lib);
		{
			SString msg, buf;
			DRVS.GetErrText(serrNotInited, msg);
			DRVS.GetErrText(-1, buf);
			msg.CatChar(':').Space().Cat(buf);
			DRVS.Log(msg, 0xffff);
		}
	ENDCATCH;
	return ok;
}

int PPDrvSberTrmnl::Release() 
{	
	CardAuth = 0;
	CloseDay = 0;
	TestPinpad = 0;
	//GlobalFree = 0;
	if(P_Lib)
		ZDELETE(P_Lib);
	return 1;
}

